const supportTopicIds = new Set<string>([
  ...CRITICAL_COOK_TOPICS,
  ...CRITICAL_ADMIN_TOPICS
]);

export const learningModules: LearningModule[] = moduleSeeds.flatMap((seed) => [
  ...createCoreModules(seed),
  ...(supportTopicIds.has(seed.topicId) ? createSupportModules(seed) : [])
]);

function createCoreModules(seed: LearningModuleSeed): LearningModule[] {
  return [
    createModule(seed, "summary", `Краткий чек по ${seed.title}`, seed.summary),
    createModule(seed, "short_module", `Короткий модуль по ${seed.title}`, seed.short),
    createModule(seed, "full_module", `Полный блок по ${seed.title}`, seed.full),
    createModule(seed, "check", `Контрольный чек по ${seed.title}`, seed.summary)
  ];
}

function createSupportModules(seed: LearningModuleSeed): LearningModule[] {
  return [
    createModule(seed, "practice", `Практический кейс по ${seed.title}`, seed.practice),
    createModule(seed, "mentor_task", `Задача наставнику по ${seed.title}`, seed.mentorTask)
  ];
}

function createModule(
  seed: LearningModuleSeed,
  format: LearningModuleFormat,
  title: string,
  content: string
): LearningModule {
  return {
    id: `lm-${seed.topicId}-${format.replace("_", "-")}`,
    role: seed.role,
    topicId: seed.topicId,
    title,
    format,
    durationMinutes: MODULE_DURATION[format],
    description: content,
    content,
    task: format === "summary" || format === "check" ? undefined : content,
    required: isRequiredTopic(seed.topicId)
  };
}

function isRequiredTopic(topicId: string) {
  return competencyTopics.some((topic) => topic.id === topicId && topic.required);
}
